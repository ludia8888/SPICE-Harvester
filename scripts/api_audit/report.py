#!/usr/bin/env python3
"""Rendering and output helpers for API duplicate/legacy audit."""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any, Sequence


def _table(rows: Sequence[Sequence[str]]) -> str:
    if not rows:
        return ""
    header = rows[0]
    body = rows[1:]
    lines = ["| " + " | ".join(header) + " |", "| " + " | ".join("---" for _ in header) + " |"]
    for row in body:
        lines.append("| " + " | ".join(row) + " |")
    return "\n".join(lines)


def _bullet_list(values: Sequence[str], *, limit: int = 15) -> str:
    if not values:
        return "- _(none)_"
    shown = list(values)[:limit]
    lines = [f"- `{value}`" for value in shown]
    if len(values) > len(shown):
        lines.append(f"- … (+{len(values) - len(shown)} more)")
    return "\n".join(lines)


def render_markdown(payload: dict[str, Any]) -> str:
    phase = str(payload.get("phase") or "phase1")
    summary = payload.get("summary", {})
    coverage = payload.get("coverage", {})
    thresholds = payload.get("thresholds", {})
    service_diffs = payload.get("service_diffs", {})
    route_stats = payload.get("route_stats", {})
    endpoints = payload.get("endpoints", [])
    clusters = payload.get("clusters", [])
    top_pairs = payload.get("top_pairs", [])

    lines: list[str] = []
    lines.append("# API Duplicate / Legacy Audit (Auto-Generated)")
    lines.append("")
    lines.append("> Generated by `scripts/api_audit`.")
    lines.append("> Do not edit manually.")
    lines.append("")
    lines.append(f"- Generated at: `{payload.get('generated_at', '-')}`")
    lines.append(f"- Phase: `{phase}`")
    lines.append("")

    lines.append("## Summary")
    lines.append("")
    lines.append(
        _table(
            [
                ("Metric", "Value"),
                ("Endpoint count", str(summary.get("endpoint_count", 0))),
                ("Cluster count", str(summary.get("cluster_count", 0))),
                ("Duplicate-equivalent clusters", str(summary.get("duplicate_equivalent_clusters", 0))),
                ("Similar-family clusters", str(summary.get("similar_family_clusters", 0))),
                ("Legacy candidates (v1 with v2 peer)", str(summary.get("legacy_candidate_count", 0))),
                ("Hidden routes", str(summary.get("hidden_route_count", 0))),
                ("High-risk endpoints", str(summary.get("high_risk_count", 0))),
            ]
        )
    )
    lines.append("")

    lines.append("## Phase 1 Acceptance Coverage")
    lines.append("")
    lines.append(
        _table(
            [
                ("Check", "Value", "Target"),
                ("No-sink endpoints (allowed)", str(coverage.get("no_sink_count", 0)), "n/a"),
                ("Sink reason missing count", str(coverage.get("sink_missing_reason_count", 0)), "0"),
                ("Hidden unclassified count", str(coverage.get("hidden_unclassified_count", 0)), "0"),
                ("Legacy unclassified count", str(coverage.get("legacy_unclassified_count", 0)), "0"),
            ]
        )
    )
    lines.append("")

    lines.append("## Runtime/Static OpenAPI Diff")
    lines.append("")
    service_rows = [("Service", "Runtime available", "Static ops", "Runtime ops", "Static-only", "Runtime-only")]
    for service, diff in sorted(service_diffs.items()):
        service_rows.append(
            (
                service,
                "yes" if diff.get("runtime_available") else "no",
                str(diff.get("static_operation_count", 0)),
                str(diff.get("runtime_operation_count", 0)),
                str(diff.get("static_only_count", 0)),
                str(diff.get("runtime_only_count", 0)),
            )
        )
    lines.append(_table(service_rows))
    lines.append("")

    lines.append("## Route Inventory (include hidden)")
    lines.append("")
    route_rows = [("Service", "Total routes", "OpenAPI routes", "Hidden routes")]
    for service, stats in sorted(route_stats.items()):
        route_rows.append(
            (
                service,
                str(stats.get("total_routes", 0)),
                str(stats.get("schema_routes", 0)),
                str(stats.get("hidden_routes", 0)),
            )
        )
    lines.append(_table(route_rows))
    lines.append("")

    lines.append("## Threshold Calibration")
    lines.append("")
    lines.append(
        _table(
            [
                ("Field", "Value"),
                ("Duplicate threshold", str(thresholds.get("duplicate_threshold", "-"))),
                ("Similar threshold", str(thresholds.get("similar_threshold", "-"))),
                ("Positive seeds", str(thresholds.get("positive_seed_count", 0))),
                ("Negative seeds", str(thresholds.get("negative_seed_count", 0))),
                ("Positive mean score", str(thresholds.get("positive_score_mean", 0))),
                ("Negative mean score", str(thresholds.get("negative_score_mean", 0))),
            ]
        )
    )
    lines.append("")

    lines.append("## High-Risk Endpoints")
    lines.append("")
    high_risk = [row for row in endpoints if str(row.get("risk_level")) == "high"]
    high_risk.sort(key=lambda row: (row.get("service", ""), row.get("path", ""), row.get("method", "")))
    lines.append(
        _table(
            [("Endpoint", "Legacy verdict", "Architecture role", "Cluster")]
            + [
                (
                    f"{row.get('service')} {row.get('method')} `{row.get('path')}`",
                    str(row.get("legacy_verdict", "-")),
                    str(row.get("architecture_role", "-")),
                    str(row.get("cluster_id", "-")),
                )
                for row in high_risk[:60]
            ]
        )
    )
    lines.append("")

    lines.append("## Duplicate-Equivalent Clusters")
    lines.append("")
    duplicate_clusters = [row for row in clusters if row.get("cluster_type") == "duplicate_equivalent"]
    duplicate_clusters.sort(key=lambda row: (row.get("risk_level", ""), row.get("cluster_id", "")), reverse=True)
    cluster_rows = [("Cluster", "Risk", "Method", "Canonical paths", "Endpoints")]
    for cluster in duplicate_clusters[:40]:
        cluster_rows.append(
            (
                str(cluster.get("cluster_id")),
                str(cluster.get("risk_level")),
                str(cluster.get("representative_method")),
                ", ".join(cluster.get("canonical_paths") or []),
                str(len(cluster.get("endpoint_ids") or [])),
            )
        )
    lines.append(_table(cluster_rows))
    lines.append("")

    lines.append("## Top Similar Pairs")
    lines.append("")
    pair_rows = [("Score", "Left endpoint", "Right endpoint")]
    for pair in top_pairs[:50]:
        pair_rows.append(
            (
                f"{float(pair.get('score', 0.0)):.3f}",
                str(pair.get("left_endpoint_id", "-")),
                str(pair.get("right_endpoint_id", "-")),
            )
        )
    lines.append(_table(pair_rows))
    lines.append("")

    phase2 = payload.get("phase2")
    if isinstance(phase2, dict):
        summary_block = phase2.get("summary", {})
        lines.append("## Phase 2 Runtime Validation")
        lines.append("")
        lines.append(
            _table(
                [
                    ("Field", "Value"),
                    ("Mode", str(phase2.get("mode", "-"))),
                    ("Risk filter", str(phase2.get("risk_filter", "-"))),
                    ("Pending reason", str(phase2.get("pending_reason", "-"))),
                    ("Total checks", f"{summary_block.get('total', 0)}/{summary_block.get('target_count', 0)}"),
                    ("Passed", str(summary_block.get("passed", 0))),
                    ("Failed", str(summary_block.get("failed", 0))),
                    ("Server errors", str(summary_block.get("server_errors", 0))),
                    ("Contract mismatches", str(summary_block.get("contract_mismatch_count", 0))),
                    ("Fallback 4xx accepted", str(summary_block.get("fallback_4xx_count", 0))),
                ]
            )
        )
        lines.append("")
        failed = [check for check in phase2.get("checks", []) if not bool(check.get("passed"))]
        lines.append("### Failed checks")
        lines.append("")
        lines.append(_bullet_list([f"{item.get('endpoint_id')} -> {item.get('status_code')}" for item in failed], limit=30))
        lines.append("")
        mismatch = [
            check
            for check in phase2.get("checks", [])
            if bool(check.get("passed")) and not bool(check.get("contract_match"))
        ]
        lines.append("### Contract drift checks (fallback 4xx)")
        lines.append("")
        lines.append(
            _bullet_list(
                [
                    f"{item.get('endpoint_id')} -> {item.get('status_code')} ({item.get('evaluation_mode')})"
                    for item in mismatch
                ],
                limit=30,
            )
        )
        lines.append("")

    lines.append("## Notes")
    lines.append("")
    lines.append("- `proxy_mirror` means BFF endpoint mirrors OMS upstream and is not auto-marked for removal.")
    lines.append("- `v1_compat_legacy_candidate` means v1 and v2 intent overlap was detected and migration planning is recommended.")
    lines.append("- Phase 2 performs HTTP-only checks and hard-fails on 5xx/request errors; contract mismatch is reported separately as drift.")
    lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, ensure_ascii=False, sort_keys=True) + "\n", encoding="utf-8")


def write_markdown(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
