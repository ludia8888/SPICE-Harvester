#!/usr/bin/env python3
"""Generate docs/index.md as a fully auto-managed Sphinx root document."""

from __future__ import annotations

import argparse
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
DEFAULT_OUTPUT = REPO_ROOT / "docs" / "index.md"

CORE_DOCS = [
    "API_REFERENCE",
    "ARCHITECTURE",
    "BACKEND_DESIGN",
    "BACKEND_METHODS",
    "ERROR_TAXONOMY",
    "FOUNDRY_ALIGNMENT_CHECKLIST",
    "FOUNDRY_V1_TO_V2_MIGRATION",
    "PROJECTION_CONSISTENCY_RUNBOOK",
]

REFERENCE_DOCS = [
    "reference/_generated/PIPELINE_MCP_TOOLS",
    "reference/_generated/PIPELINE_AGENT_ALLOWED_TOOLS",
    "reference/_generated/ONTOLOGY_AGENT_ALLOWED_TOOLS",
    "reference/_generated/API_VERSION_OVERLAP",
    "reference/_generated/REPO_FILE_INVENTORY",
]

ONBOARDING_KO_DOCS = [
    "onboarding/ko/01-WHAT-IS-SPICE",
    "onboarding/ko/02-MENTAL-MODEL",
    "onboarding/ko/03-LOCAL-SETUP",
    "onboarding/ko/04-FIRST-API-CALL",
    "onboarding/ko/05-ARCHITECTURE-EXPLAINED",
    "onboarding/ko/06-DATA-FLOW-WALKTHROUGH",
    "onboarding/ko/07-FRONTEND-TOUR",
    "onboarding/ko/08-DEVELOPMENT-WORKFLOW",
    "onboarding/ko/09-TESTING-GUIDE",
    "onboarding/ko/10-TROUBLESHOOTING-FAQ",
    "onboarding/ko/11-REAL-WORLD-SCENARIOS",
    "onboarding/ko/12-ACTION-TYPES-DEEP-DIVE",
    "onboarding/ko/LEARNING-ROADMAP",
]


def _render() -> str:
    lines: list[str] = []
    lines.append("# Documentation Index (Auto-Generated)")
    lines.append("")
    lines.append("> Generated by `scripts/generate_docs_index.py`. Do not edit manually.")
    lines.append("")
    lines.append("## New Developer Start Here")
    lines.append("")
    lines.append("> [!TIP]")
    lines.append("> Recommended reading order for first-time onboarding (about 60-90 minutes).")
    lines.append("")
    lines.append("| Step | Document | Why It Matters |")
    lines.append("| --- | --- | --- |")
    lines.append("| 1 | `ARCHITECTURE` | Runtime topology, service dependencies, and critical data/event flows. |")
    lines.append("| 2 | `API_REFERENCE` | Actual contract surface (/api/v1, /api/v2) with endpoint-level metadata. |")
    lines.append("| 3 | `BACKEND_DESIGN` | Module responsibilities, risk hotspots, and read-order guidance. |")
    lines.append("| 4 | `FOUNDRY_V1_TO_V2_MIGRATION` | Migration rules and compatibility decisions. |")
    lines.append("| 5 | `ERROR_TAXONOMY` | Error contract expectations and failure classification. |")
    lines.append("")

    lines.append("```{toctree}")
    lines.append(":maxdepth: 2")
    lines.append(":caption: Core")
    lines.append("")
    lines.extend(CORE_DOCS)
    lines.append("```")
    lines.append("")

    lines.append("```{toctree}")
    lines.append(":maxdepth: 2")
    lines.append(":caption: Reference")
    lines.append("")
    lines.extend(REFERENCE_DOCS)
    lines.append("```")
    lines.append("")

    lines.append("```{toctree}")
    lines.append(":maxdepth: 2")
    lines.append(":caption: Onboarding (한국어)")
    lines.append("")
    lines.extend(ONBOARDING_KO_DOCS)
    lines.append("```")
    lines.append("")

    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--output",
        type=Path,
        default=DEFAULT_OUTPUT,
        help="Output markdown path (default: docs/index.md)",
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help="Exit non-zero if output would change",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    content = _render()
    existing = args.output.read_text(encoding="utf-8") if args.output.exists() else ""

    if args.check:
        if existing != content:
            print(f"{args.output} is out of date. Run: python scripts/generate_docs_index.py")
            return 1
        return 0

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(content, encoding="utf-8")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
